def showme_fruits():#Cambiamos el nombre de la funcion para ser mas descriptivos
        """
        Basic example of a for loop:
        Iterates through a list of fruits and prints them.
        """
        fruits_list = ["apple", "banana", "coco"] #Le cambiamos el nombre de platano a coco porque hace mas sentido que todo este en el mismo idioma
        for fruit in fruits_list: #cambie el nombre de fruits a fruits_list para ser mas descritivo
            print(f"The current fruit is: {fruit}")

showme_fruits()
